    <header class="mb-5 text-center">
        <p class="text-lg tracking-wide text-sky-950/85 sm:text-xl">༄༅། །སྐྱབས་འགྲོ་སེམས་བསྐྱེད།</p>
        <h3 class="mt-3 text-base font-medium not-italic sm:text-lg">Verses of Refuge and Bodhichitta</h3>
        <p class="mt-1 text-xs not-italic text-sky-950/60">by Śākyaśrībhadra</p>
    </header>

    <div class="space-y-5">
        <div>
            <p class="text-xs not-italic text-sky-950/55">dro nam dral dö sampa yi</p>
            <p class="mt-0.5 italic">Motivated by the wish to free all beings,</p>

            <p class="mt-3 text-xs not-italic text-sky-950/55">sangye chö dang gendün la</p>
            <p class="mt-0.5 italic">Continually, I shall take refuge</p>

            <p class="mt-3 text-xs not-italic text-sky-950/55">changchub nyingpor chi kyi bar</p>
            <p class="mt-0.5 italic">In the Buddha, Dharma and Saṅgha,</p>

            <p class="mt-3 text-xs not-italic text-sky-950/55">takpar dak ni kyab su chi</p>
            <p class="mt-0.5 italic">Till the essence of enlightenment is reached.</p>
        </div>

        <div>
            <p class="text-xs not-italic text-sky-950/55">sherab nying tsé dang chepé</p>
            <p class="mt-0.5 italic">With wisdom and with heartfelt love,</p>

            <p class="mt-3 text-xs not-italic text-sky-950/55">tsönpé semchen döndu dak</p>
            <p class="mt-0.5 italic">Diligently, for the sake of sentient beings,</p>

            <p class="mt-3 text-xs not-italic text-sky-950/55">sangye dündu né gyi né</p>
            <p class="mt-0.5 italic">I shall place myself before the buddhas,</p>

            <p class="mt-3 text-xs not-italic text-sky-950/55">dzokpé changchub semkyé do</p>
            <p class="mt-0.5 italic">And generate the perfect mind of bodhicitta.</p>
        </div>
    </div>

    <p class="mt-6 text-center text-xs not-italic text-sky-950/65">
        Composed by the great Kashmiri paṇḍita Śākyaśrībhadra.
    </p>

