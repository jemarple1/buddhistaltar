        <h3 class="text-base font-medium not-italic sm:text-lg">The Dhāraṇī of Noble Avalokiteśvara</h3>
        <p class="mt-1 text-xs not-italic text-sky-950/60">from the Words of the Buddha</p>
    </header>

    <p class="mb-3 italic">Homage to noble Avalokiteśvara, the embodiment of great compassion!</p>

    <p class="mb-3">Out of great love and the force of his boundless compassion, noble Avalokiteśvara generated bodhicitta, the heart of awakening. Seated upon a throne of lotus, sun, and moon he appeared through the emanation and reabsorption of light from the syllable hrīḥ. He manifests with one face and four arms: His first two hands are joined in the gesture of prayer. His lower right hand holds a white pearl mālā; his lower left, a white lotus. His body, white in colour, is seated in the full vajra posture, adorned with precious ornaments and a resplendent crown. At his side stand the mother Ṣaḍakṣarī and the father Maṇidhara. Around him gather countless male and female bodhisattvas, forming a vast retinue. Thus it was spoken:</p>

    <p class="mb-3 italic">O sons and daughters of noble family!</p>

    <p class="mb-3">Whether you recite my mantra but once, whether you hold it in your heart with steady recollection, or write it down and carry it upon your body—if done with faith and devotion, you will purify the five gravest misdeeds and the negative actions that are almost as grave. You will be freed from the three lower realms and the eight situations that are inopportune for Dharma practice. You will never again be born into suffering. All harm from humans, non-humans, and wild beasts will be pacified. You will be released from all illnesses and obstructing influences.</p>

    <p class="my-4 text-center text-lg tracking-wide">oṃ maṇi padme hūṃ</p>

    <p class="mb-3">By reciting this sacred mantra, you shall behold the face of noble Avalokiteśvara, and at the end of this life, you will be reborn in the pure land of Sukhāvatī. Have no hesitation, uncertainty, or doubt about this truth.</p>

    <p class="mb-4 italic">This concludes the Dhāraṇī of Noble Avalokiteśvara.</p>

