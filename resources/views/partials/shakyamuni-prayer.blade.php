    <header class="mb-4 text-center">
        <h3 class="text-base font-medium not-italic sm:text-lg">Our Compassionate Teacher, Śākyamuni</h3>
        <p class="mt-1 text-xs not-italic text-sky-950/60">from Verses of Homage to the Buddhas and Bodhisattvas</p>
        <p class="mt-1 text-xs not-italic text-sky-950/60">by Kyabjé Trulshik Rinpoche</p>
    </header>

    <p class="mb-3 italic">With your great compassion, you embraced this turbulent and degenerate world, and made five hundred mighty aspirations. You are as exalted as the white lotus; whoever hears your name shall never return to saṃsāra—most compassionate teacher, to you I pay homage!</p>

    <p class="mb-4 italic">Supreme teacher, bhagavān, tathāgata, arhat, complete and perfect buddha, glorious conqueror, Śākyamuni, to you I bow! To you I pay homage! To you I make offerings! In you I take refuge! Grant your blessings, I pray!</p>

    <p class="my-4 text-center text-lg tracking-wide">tadyathā oṃ mune mune mahāmunaye svāhā</p>

