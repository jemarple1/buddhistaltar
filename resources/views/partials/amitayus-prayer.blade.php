    <header class="mb-4 text-center">
        <h3 class="text-base font-medium not-italic sm:text-lg">A Prayer to Amitāyus</h3>
        <p class="mt-1 text-xs not-italic text-sky-950/60">by Shechen Gyaltsab Pema Namgyal</p>
    </header>

    <p class="mb-3 italic">Hearing your name, O Bhagavān Amitāyus,</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">gang tsen töpé dümin chiwé pung</p>

    <p class="mb-3 italic">Instantly destroys the armies of maras who bring untimely death.</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">chikchar zhom dzé chomden tsepakmé</p>

    <p class="mb-3 italic">In you I take refuge! To you I pray from the depth of my heart:</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">khyé la kyab chi nying né solwa deb</p>

    <p class="mb-3 italic">Bless me, so I may attain supreme vajra-like longevity!</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">dorjé tsé chok drubpar jingyi lob</p>

    <p class="my-4 text-center text-lg tracking-wide">oṃ amaraṇi jīvantaye svāhā</p>
    <p class="mb-1 text-center text-xs not-italic text-sky-950/55">om amarani dziwentiyé soha</p>

    <p class="mb-4 text-sm italic">By Vijaya.</p>

