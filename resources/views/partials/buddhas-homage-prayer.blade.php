    <header class="mb-6 text-center">
        <h3 class="text-base font-medium not-italic sm:text-lg">Verses of Homage to the Buddhas and Bodhisattvas Together with their Mantras</h3>
        <p class="mt-1 text-xs not-italic text-sky-950/60">Arranged for Recitation by Practitioners of All Levels</p>
        <p class="mt-1 text-xs not-italic text-sky-950/60">by Kyabjé Trulshik Rinpoche</p>
    </header>

    <section id="shakyamuni" class="homage-section">
        <h4 class="homage-section-title">Our Compassionate Teacher, Śākyamuni</h4>
        <p class="italic">With your great compassion, you embraced this turbulent and degenerate world, and made five hundred mighty aspirations. You are as exalted as the white lotus; whoever hears your name shall never return to saṃsāra—most compassionate teacher, to you I pay homage!</p>
        <p class="mt-3 italic">Supreme teacher, bhagavān, tathāgata, arhat, complete and perfect buddha, glorious conqueror, Śākyamuni, to you I bow! To you I pay homage! To you I make offerings! In you I take refuge! Grant your blessings, I pray!</p>
        <p class="my-3 text-center tracking-wide">tadyathā oṃ mune mune mahāmunaye svāhā</p>
    </section>

    <section id="vajrasattva" class="homage-section">
        <h4 class="homage-section-title">Glorious Vajrasattva</h4>
        <p class="italic">You are the primordial awareness of skilful means—the indestructible state beyond all concepts, realized in the nature of the Great Mother, transcendental wisdom free from any reference, displaying your compassion, in all its variety, in every kind of way—O Great Vajrasattva, to you I pay homage!</p>
        <p class="my-3 text-center tracking-wide">oṃ vajrasattva āḥ</p>
    </section>

    <section id="amitayus" class="homage-section">
        <h4 class="homage-section-title">Amitāyus, Buddha of Limitless Life and Wisdom</h4>
        <p class="italic">Buddha of Infinite Life, foremost guide for beings in this world, your glory overcomes all untimely death, you are a refuge for those of us who suffer without protection—to you, Buddha Amitāyus, I pay homage!</p>
        <p class="my-3 text-center tracking-wide">oṃ amaraṇi jīvantaye svāhā</p>
    </section>

    <section id="amitabha" class="homage-section">
        <h4 class="homage-section-title">Amitābha, Buddha of Limitless Light</h4>
        <p class="italic">In the realm of Sukhāvatī, you turn the wheel of Dharma, gazing on living beings with all your compassion, and acting for their benefit, just as you vowed—to you, Amitābha resting in meditation, I pay homage!</p>
        <p class="my-3 text-center tracking-wide">oṃ amitābha hrīḥ</p>
    </section>

    <section id="ratnasikhin" class="homage-section">
        <h4 class="homage-section-title">Buddha Ratnaśikhin</h4>
        <p class="italic">Bhagavān, tathāgata, arhat, complete and perfect buddha, Ratnaśikhin, to you I bow! To you I pay homage! In you I take refuge! Grant your blessings, I pray!</p>
        <p class="my-3 text-center tracking-wide">tadyathā ratna ratna ratnaśikhene svāhā</p>
    </section>

    <section id="medicine-buddha" class="homage-section">
        <h4 class="homage-section-title">Blessed Bhaiṣajya Guru, Buddha of Medicine</h4>
        <p class="italic">Blessed one, whose compassion for all is equal, simply hearing your name dispels the suffering of lower realms, Buddha of Medicine, you who heal the sickness of the three poisons—Light of Lapis Lazuli, to you I pay homage!</p>
        <p class="mt-3 italic">Bhagavān, tathāgata, arhat, complete and perfect buddha, Buddha of Medicine, Radiant Light of Lapis Lazuli King, to you I bow! To you I pay homage! In you I take refuge!</p>
        <p class="my-3 text-center tracking-wide">tadyathā oṃ bhaiṣajye bhaiṣajye mahābhaiṣajye bhaiṣajyarājasamudgate svāhā</p>
    </section>

    <section id="maitreya" class="homage-section">
        <h4 class="homage-section-title">Buddha's Regent, the Protector Maitreya</h4>
        <p class="italic">The fire of your great love burns up the dry wood of hate, the light of your wisdom dispels the darkness of ignorance, Dharma regent, protector of all living beings, who dwells in the Tuṣita heaven—to you I pay homage!</p>
        <p class="my-3 text-center tracking-wide">oṃ mati mati smṛti svāhā</p>
    </section>

    <section id="avalokiteshvara" class="homage-section">
        <h4 class="homage-section-title">Noble Avalokiteśvara</h4>
        <p class="italic">Your thousand arms are the thousand universal monarchs, your thousand eyes the thousand buddhas of this fortunate age, you who teach each and every one of us according to our needs—Lord Avalokiteśvara, to you I pay homage!</p>
        <p class="my-3 text-center tracking-wide">oṃ maṇi padme hūṃ hrīḥ</p>
        <p class="text-xs not-italic text-sky-950/60">It is said to be acceptable to add the final syllable hrīḥ.</p>
    </section>

    <section id="manjushri" class="homage-section">
        <h4 class="homage-section-title">Noble Lord Mañjuśrī</h4>
        <p class="italic">Across the skies of all that can be known, profound and infinite, shine vast rays of light from the sun of your intelligence, dispelling the darkness of ignorance in all beings' minds—Lord Mañjughoṣa, to you I pay homage!</p>
        <p class="my-3 text-center tracking-wide">oṃ arapacana dhīḥ</p>
    </section>

    <section id="vajravidarana" class="homage-section">
        <h4 class="homage-section-title">Vajravidāraṇa</h4>
        <p class="italic">With the non-dual wisdom of your enlightened mind, and through the power of your blazing vajra and jewel, you vanquish completely the forces of the four māras—Vajravidāraṇa, to you we offer homage and praise!</p>
        <p class="mt-3 italic">All the buddhas' power and strength is condensed within you alone, who manifest as the enlightened form of vajra wrath—Vajravidāraṇa, to you I pay homage!</p>
        <p class="my-3 text-center text-xs tracking-wide">namaścaṇḍa vajrakrodhāya | hulu hulu | tiṣṭha tiṣṭha | bhandha bhandha | hana hana | amṛte hūṃ phaṭ</p>
    </section>

    <section id="vijaya" class="homage-section">
        <h4 class="homage-section-title">Noble Vijayā, Goddess of Victory</h4>
        <p class="italic">Glorious goddess, your colour that of the autumn moon, with three faces and eight arms, your form ravishing and serene, you grant the supreme gifts of longevity and boundless wisdom—Noble Vijayā, to you I pay homage!</p>
        <p class="my-3 text-center tracking-wide">oṃ amṛtāyurdade svāhā</p>
    </section>

    <section id="tara" class="homage-section">
        <h4 class="homage-section-title">Noble Saviouress Tārā</h4>
        <p class="italic">Deity on whom I meditated in lives gone by, you are the enlightened activity of all buddhas, past, present, and future, brilliant white, with your one face, two hands, and seven eyes, Mother of the buddhas, holder of the utpala flower, to you I pay homage!</p>
        <p class="my-3 text-center tracking-wide">oṃ tāre tuttāre ture svāhā</p>
    </section>

    <section id="guru-rinpoche" class="homage-section">
        <h4 class="homage-section-title">Prayer to the Guru, the Embodiment of All the Buddhas</h4>
        <p class="italic">To the eight manifestations of Guru Rinpoche, the eight great accomplished vidyādharas, the eight great bodhisattvas, the “close sons,” and the eight maṇḍalas of Kagyé with all their deities—to you I pray—inspire me with your blessings! Dispel all obstacles outer, inner and secret! Fulfill all my aspirations! Grant us attainments, ordinary and supreme!</p>
        <p class="my-3 text-center tracking-wide">oṃ āḥ hūṃ vajra guru padma siddhi hūṃ</p>
    </section>

    <section id="hayagriva" class="homage-section">
        <h4 class="homage-section-title">The Wrathful Union of Hayagrīva, Vajrapāṇi and Garuḍa</h4>
        <p class="italic">You are the wisdom body, speech and mind of all the buddhas of the ten directions, the kingly Garuḍa, Hayagrīva and the Lord of Secrets manifesting as one deity, foremost among the wrathful. The very thought of you crushes obstructing forces—and to you I offer homage and praise!</p>
        <p class="my-3 text-center tracking-wide">oṃ vajrapāṇi hayagrīva garuḍa hūṃ phaṭ</p>
    </section>

    <section class="homage-section">
        <h4 class="homage-section-title">Dedication</h4>
        <p class="italic">Through this merit, may I swiftly accomplish the realization of the buddhas and their bodhisattva heirs, and may I bring each and every single living being to that perfect state as well.</p>
        <p class="mt-4 italic">Bodhicitta, precious and sublime: may it arise in those in whom it has not arisen; may it never decline where it has arisen; may it go on increasing, further and further!</p>
        <p class="mt-4 italic">The precious view of śūnyatā: may it be realized by those who have not realized it; may it never decline where it has been realized; may it go on increasing, further and further!</p>
    </section>

    <footer class="mt-8 border-t border-sky-900/10 pt-4 text-xs not-italic leading-normal text-sky-950/55">
        <p>For all of you who have received teachings from me and have a Dharma connection with me, it would be best to recite all these prayers and mantras every day. Otherwise, recite whichever you wish, especially on the 8th, 15th and 30th days of the lunar month, and on the four great anniversaries of our Teacher Śākyamuni. Whenever you do so, the benefit will be immeasurable. I, the Zhadeupa Ngawang Chökyi Lodrö, an ignorant monk of Śākyamuni, entreat you with folded hands.</p>
