    <header class="mb-4 text-center">
        <h3 class="text-base font-medium not-italic sm:text-lg">Prayer to Buddha Amitābha</h3>
        <p class="mt-1 text-xs not-italic text-sky-950/60">by the Fifteenth Karmapa, Khakhyab Dorje</p>
    </header>

    <p class="mb-3 italic">O dharmakāya Amitābha, embodiment of all the buddhas,</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">gyal kun chi zuk chö ku ö ta yé</p>

    <p class="mb-3 italic">And protector Avalokiteśvara, manifestation of compassion,</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">tukjé rang tsal gönpo chenrezik</p>

    <p class="mb-3 italic">To these ultimate and supreme sources of refuge, I pray!</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">kyabné tartuk chok la solwa dep</p>

    <p class="mb-3 italic">May I and all beings, my previous mothers, as infinite as space,</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">dak dang kha nyam ma gen semchen nam</p>

    <p class="mb-3 italic">Face no unfavourable conditions, only ever-increasing positivity.</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">tsé dir midö kyen zhi lek tsok gyé</p>

    <p class="mb-3 italic">May we be free from misdeeds and have the eight qualities and ten virtues;</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">dikdral yönten gyé dang gé chu jor</p>

    <p class="mb-3 italic">And, in our future lives, may we travel to the pure realm of Great Bliss!</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">chimar dechen dakpé zhing du drö</p>

    <p class="mb-3 italic">Bless us, so that having attained buddhahood in that physical form,</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">lü ten dé la chang chub chok tob né</p>

    <p class="mb-3 italic">We may become a guide who leads beings from extremes,</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">ta lé drowa dren la gön khyé dang</p>

    <p class="mb-3 italic">And gain strength and power just like yours.</p>
    <p class="mb-1 text-xs not-italic text-sky-950/55">nyampé tu wang tobpar chin gyi lop</p>

    <p class="mb-4 italic">Supplicate like this with a one-pointed mind, and recite:</p>

    <p class="my-4 text-center text-lg tracking-wide">oṃ amitābha hrīḥ</p>
    <p class="mb-1 text-center text-xs not-italic text-sky-950/55">om ami dhewa hrih</p>

    <p class="mb-4 text-sm italic">Thus, in response to a request from Demchok Lhamo of noble family, the one known as Karmapa put this into writing.</p>

