    <header class="mb-4 text-center">
        <h3 class="text-base font-medium not-italic sm:text-lg">In Praise of Glorious Vajrasattva</h3>
        <p class="mt-1 text-xs not-italic text-sky-950/60">by Patrul Rinpoche</p>
    </header>

    <p class="mb-4 text-center font-medium not-italic">Homage to glorious Vajrasattva!</p>

    <p class="mb-3 italic">Like a mountain of snow, your body is brilliant white,</p>
    <p class="mb-3 italic">And your enlightened speech resounds as perfect Dharma,</p>
    <p class="mb-3 italic">Profound and expansive, your sky-like mind is twofold wisdom,</p>
    <p class="mb-4 italic">Blessed lord, spiritual warrior—to you I pay homage!</p>

    <p class="mb-3 italic">Even negative karma and obscurations accumulated over many eons</p>
    <p class="mb-3 italic">Are reduced to nothing in an instant merely by recalling your name,</p>
    <p class="mb-3 italic">Guardian Lord, there is no one anywhere to compare with you,</p>
    <p class="mb-4 italic">And so, again and again, in devotion I pay homage!</p>

    <p class="mb-3 italic">Deities of the maṇḍalas of the hundred, five or three buddha families,</p>
    <p class="mb-3 italic">And the lord of the sixth—they are all none other than you.</p>
    <p class="mb-3 italic">And it was you too who gathered the words of the buddha, the secret treasury—</p>
    <p class="mb-4 italic">Embodiment of all the victorious buddhas, to you I pay homage!</p>

    <p class="mb-3 italic">Upon a perfect moon-disc symbolising your plentiful qualities,</p>
    <p class="mb-3 italic">And an eight-petalled white lotus signifying freedom from flaws,</p>
    <p class="mb-3 italic">You sit in cross-legged posture, abiding in neither saṃsāra nor nirvāṇa—</p>
    <p class="mb-4 italic">To you who wear anklets of jewels, I pay homage!</p>

    <p class="mb-3 italic">Beautiful like a rainbow with your silken garments of many colours,</p>
    <p class="mb-3 italic">And your jewels, necklaces and other adornments all sparkling and swaying,</p>
    <p class="mb-3 italic">You hold the vajra and bell symbolising appearance and emptiness inseparable—</p>
    <p class="mb-4 italic">To you in whom the empowerment of the five families is complete, I pay homage!</p>

    <p class="mb-3 italic">Your body is the bindu of wisdom and great bliss united,</p>
    <p class="mb-3 italic">And with the abhiṣeka nectar flowing from clouds of joy,</p>
    <p class="mb-3 italic">You confer the amṛta of the utterly pure five senses—</p>
    <p class="mb-4 italic">To you who delight in non-duality, I pay homage!</p>

    <p class="mb-3 italic">The very nature of awareness, the crucial point of essence mantra,</p>
    <p class="mb-3 italic">Which you taught in the realm of Akaniṣṭha,</p>
    <p class="mb-3 italic">And which, when recited or brought to mind, purifies all impairments and breakages—</p>
    <p class="mb-4 italic">To your innermost essence, the hundred-syllable mantra, I pay homage!</p>

    <p class="mb-3 italic">For calling out to you by name and invoking your heart commitment,</p>
    <p class="mb-3 italic">With the nature of the five families, the heart of the innermost essence,</p>
    <p class="mb-3 italic">In which causes and conditions, recitation and mantra are all complete,</p>
    <p class="mb-4 italic">To your brief six-syllable mantra, I pay homage!</p>

    <p class="mb-3 italic">You are the deity who subdues the unruly and difficult to tame,</p>
    <p class="mb-3 italic">And excellently bestows the supreme accomplishment,</p>
    <p class="mb-3 italic">Lord Vajrasattva, who displays the nine moods of dance—</p>
    <p class="mb-4 italic">To you, the glorious heruka, I pay homage!</p>

    <p class="mb-3 italic">Keeper of the secret treasury, appearing as a powerful lord of the tenth bhūmi,</p>
    <p class="mb-3 italic">Ferocious Vajrapāṇi—sole enemy of obstructing forces,</p>
    <p class="mb-3 italic">With your garuḍa, whose nature is of the five families, emanating and returning—</p>
    <p class="mb-4 italic">To you, subjugator of the nāgas of ignorance, I pay homage!</p>

    <p class="mb-3 italic">Supreme deity of deities, bhagavān Vajrasattva,</p>
    <p class="mb-3 italic">Taking you, O protector, as my supreme refuge,</p>
    <p class="mb-3 italic">I think of your enlightened body, speech and mind again and again,</p>
    <p class="mb-4 italic">Now inspire me with your blessings, O Vajrasattva, Vajradharma!</p>

    <p class="my-4 text-center text-lg tracking-wide">oṃ vajra sattva hūṃ</p>

    <p class="mb-4 text-center text-sm italic">by Patrul</p>
    <p class="mb-4 text-center text-sm italic">Virtue. Virtue. Virtue.</p>

