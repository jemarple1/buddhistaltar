        <h2 id="offering-benefits-heading" class="text-sm tracking-[0.25em] uppercase text-sky-950/80">On Offering Before the Buddhas</h2>
        <p class="mt-2 text-xs not-italic text-sky-950/60">A rimé gathering of shrine rooms for the living and the deceased</p>
    </header>

    <p class="italic text-sky-950/85">
        Buddhist masters throughout India and Tibet teach that offering before the buddhas and bodhisattvas opens the heart, gathers merit, and supports both the living and those who have passed. This platform is rimé—non-sectarian—so practitioners of every lineage may enter a shrine, make offerings in person or in imagination, and dedicate the virtue for the benefit of all.
    </p>

    <blockquote class="offering-benefit-quote">
        <p class="italic">“Every thing and place of beauty unclaimed by any owner, extending to the boundless limits of space itself—I picture them all in my mind, and to the supreme buddhas and their bodhisattva heirs, I make a perfect gift of them.”</p>
        <footer>— Śāntideva, <cite>Bodhicaryāvatāra</cite>, ch. 2; trans. Adam Pearcey (<a href="https://www.lotsawahouse.org/indian-masters/shantideva/bodhicharyavatara-2" class="underline decoration-sky-950/30 hover:decoration-sky-950/60" rel="noopener noreferrer" target="_blank">Lotsawa House</a>)</footer>
    </blockquote>

    <p>
        Mahāyāna practice therefore praises offerings made in the mind as well as on the altar. Patrul Rinpoche’s guide to meditating on Śāntideva’s text instructs practitioners to set out what they can on the shrine, then offer the whole world in imagination.
    </p>

    <blockquote class="offering-benefit-quote">
        <p class="italic">“Arrange offerings of flowers, scented incense, lamps, pure water and food and drink, as plentiful as you can afford. Then bring to mind jewel-filled mountains, attractive woodlands, and all the uninhabited places of great natural beauty throughout the world. These are known as ‘offerings you do not own’.”</p>
        <footer>— Patrul Rinpoche, <cite>The Brightly Shining Sun</cite>; trans. Adam Pearcey (<a href="https://www.lotsawahouse.org/tibetan-masters/patrul-rinpoche/bodhicharyavatara-brightly-shining-sun" class="underline decoration-sky-950/30 hover:decoration-sky-950/60" rel="noopener noreferrer" target="_blank">Lotsawa House</a>)</footer>
    </blockquote>

    <p>
        A butter lamp, flower, or bowl of water placed here—or visualized with devotion—can be offered for oneself, for someone who is ill, or for someone who has died. The <cite>Sūtra of the Past Vows of Earth Store Bodhisattva</cite> (Kṣitigarbha Sūtra) teaches how merit performed on a deceased person’s behalf can reach them in the bardo and beyond.
    </p>

    <blockquote class="offering-benefit-quote">
        <p class="italic">“When men or women laden with offenses who failed to plant good causes die, even they can receive one-seventh of any merit dedicated to them by relatives who do good deeds on their behalf.”</p>
        <footer>— <cite>Sūtra of the Past Vows of Earth Store Bodhisattva</cite>, ch. 7, “Benefiting the Living and the Dead”; trans. Buddhist Text Translation Society (<a href="https://www.cttbusa.org/esscommentary/earthstore_preface.htm" class="underline decoration-sky-950/30 hover:decoration-sky-950/60" rel="noopener noreferrer" target="_blank">CTTB</a>)</footer>
    </blockquote>

    <blockquote class="offering-benefit-quote">
        <p class="italic">“Throughout forty-nine days, those whose lives have ended and who have not yet been reborn will be hoping every moment that their immediate relatives will earn blessings powerful enough to rescue them.”</p>
        <footer>— <cite>Sūtra of the Past Vows of Earth Store Bodhisattva</cite>, ch. 7; trans. Buddhist Text Translation Society</footer>
    </blockquote>

    <p>
        Patrul Rinpoche also reminds us not to dismiss small acts of virtue—an encouragement for anyone offering what they can from home or online.
    </p>

    <blockquote class="offering-benefit-quote">
        <p class="italic">“Do not take lightly small good deeds, believing they can hardly help: for drops of water one by one in time can fill a giant pot.”</p>
        <footer>— <cite>Sūtra of the Wise and the Foolish</cite>, as cited by Patrul Rinpoche in <cite>Words of My Perfect Teacher</cite>; trans. Padmakara Translation Group (Boston: Shambhala, 1998)</footer>
    </blockquote>

    <p>
        Whether one sits before a physical shrine or enters an online altar such as these, the practice is the same: generate bodhicitta, offer what one can, recite prayers or mantras as one wishes, and seal the merit with dedication. Śāntideva teaches that the wish to benefit others is itself a supreme offering.
    </p>

    <blockquote class="offering-benefit-quote">
        <p class="italic">“For if the simple wish to benefit others surpasses offerings made before the buddhas, what need is there to mention striving for the welfare of all without exception?”</p>
        <footer>— Śāntideva, <cite>Bodhicaryāvatāra</cite>, ch. 1; trans. Adam Pearcey (<a href="https://www.lotsawahouse.org/indian-masters/shantideva/bodhicharyavatara-1" class="underline decoration-sky-950/30 hover:decoration-sky-950/60" rel="noopener noreferrer" target="_blank">Lotsawa House</a>)</footer>
    </blockquote>

    <blockquote class="offering-benefit-quote">
        <p class="italic">“If you fail to seal your merit with authentic dedication, then however vast the offerings and positive actions you have performed may be, their results can only be ephemeral and vulnerable to the destructive effects of your negative emotions, such as anger, pride, and jealousy.”</p>
        <footer>— Dilgo Khyentse Rinpoche, <cite>The Heart of Compassion</cite>: <cite>The Thirty-seven Verses on the Practice of a Bodhisattva</cite>; trans. Padmakara Translation Group (Boston: Shambhala, 2007)</footer>
    </blockquote>

    <p>
        You may enter any buddha or bodhisattva above to light a lamp, offer incense, water, flowers, or music, read that shrine’s prayer, and dedicate the merit at the end of your session. For those who have died, offer with their name in mind—or without name, on behalf of all beings in the six realms—and conclude with the dedication of merit. Offerings made here remain for twenty-four hours as a shared field of merit among practitioners worldwide.
    </p>
